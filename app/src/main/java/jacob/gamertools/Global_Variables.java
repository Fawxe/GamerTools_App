public class Global_Variables

{

    public int large_increase=10;
    public int small_increase=1;
    public int small_decrease=1;
    public int large_decrease=10;

    public void setSmall_increase(int newValue)
    {small_increase=newValue;}

    public int getSmall_increase(){return small_increase;}

    public void setLarge_increase(int newValue)
    {large_increase=newValue;}

    public int getLarge_increase(){return large_increase;}

    public void setSmall_decrease(int newValue)
    {small_decrease=newValue;}

    public int getSmall_decrease(){return small_decrease;}

    public void setLarge_decrease(int newValue)
    {large_decrease=newValue;}

    public int getLarge_decrease(){return large_decrease;}

}
